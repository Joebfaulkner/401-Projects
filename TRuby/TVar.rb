# Ruby does not require data types to be declared

$IntVar = 5
$DoubleVar = 0.05
$FloatVar = 5.55555
$BooleanVar = true
$StringVar = "This is a variable"

puts("IntVar = #$IntVar")
puts("DoubleVar = #$DoubleVar")
puts("FloatVar = #$FloatVar")
puts("BooleanVar = #$BooleanVar")
puts("StringVar contains the string #$StringVar")
puts("IntVar + DoubleVar = #{$IntVar + $DoubleVar}")
puts("FloatVar + DoubleVar = #{$FloatVar + $DoubleVar}")
puts("FloatVar + DoubleVar + IntVar = #{$FloatVar + $DoubleVar + $IntVar}")